package day3;
//Patient record system: protect patient health info
public class en31 {
	
	    private String name;
	    private int age;
	    private String healthInfo;  

	    public void setName(String n) {
	        name = n;
	    }

	    public void setAge(int a) {
	        age = a;
	    }

	    public void setHealthInfo(String info) {
	        healthInfo = info;
	    }

	    public String getName() {
	        return name;
	    }

	    public int getAge() {
	        return age;
	    }

	    public String getHealthInfo() {
	        return healthInfo;
	    }
	


}
